    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
       
       //to check if all fields are filled
       if(txtName.getText().equals("") || txtCardNumber.getText().equals("") || txtEmail.getText().equals("")){
           JOptionPane.showMessageDialog(null, "All fields must be filled!", "Attention", JOptionPane.WARNING_MESSAGE);
           return;       
        } 
       
       //to export the export movie's name and customer's name to the receipt and open the Rent Confirmation page
       model.Movie model = new model.Movie();
       model.Movie model2 = new model.Movie();
       model.setCustomer(txtName.getText());
       model2.setName(RentMovie.txtMovies.getText());
       RentConfirmation RentConfirmation = new RentConfirmation();
       RentConfirmation.customerExport(model);
       RentConfirmation.movieExport(model2);
       RentConfirmation.setVisible(true);      
       dispose();
        
        
        
    }//GEN-LAST:event_btnPayActionPerformed

