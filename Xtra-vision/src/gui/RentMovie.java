    //method to fill the movies table and show it to the user    
    public void fillTable(String Sql){
        
        ArrayList data = new ArrayList();
        String [] columns = new String []{"ID","Name","Price"};
        conn.getConnection();
//        conn.executeSQl();
        try{
            conn.rs.first();
            do{
            
               data.add(new Object[]{conn.rs.getInt("id_movie"),conn.rs.getString("name"),conn.rs.getFloat("price")});
                
            }while(conn.rs.next());
        
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Error when filling the array list!"+ex);
        }
        
        MovieTable model = new MovieTable(data, columns);
        
        tableMovies.setModel(model);
        tableMovies.getColumnModel().getColumn(0).setPreferredWidth(23);
        tableMovies.getColumnModel().getColumn(0).setResizable(false);
        tableMovies.getColumnModel().getColumn(1).setPreferredWidth(220);
        tableMovies.getColumnModel().getColumn(1).setResizable(false);
        tableMovies.getColumnModel().getColumn(2).setPreferredWidth(80);
        tableMovies.getColumnModel().getColumn(2).setResizable(false);
        tableMovies.getTableHeader().setReorderingAllowed(false);
        tableMovies.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                
        conn.disconnect();
    }
    
    
    
      
    
